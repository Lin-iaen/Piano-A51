; ║  第1部分：文件名片                                                     ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  文件名: Drv_Timer.asm                                                ║
; ║  层级:   Drv (驱动层)                                                 ║
; ║  功能:   Timer0的初始化和中断服务。Timer0被用作蜂鸣器的"音调发生器"——   ║
; ║         设定一个频率初值，每次定时器溢出时翻转P2.4引脚，产生方波。       ║
; ║                                                                      ║
; ║  白话:   蜂鸣器发声需要方波（电信号快速地高-低-高-低切换）。             ║
; ║         怎么产生方波？让Timer0像一个"自动闹钟"，每隔固定时间就响一次，   ║
; ║         响了（溢出=闹钟触发中断）就把P2.4翻转一下。                     ║
; ║         翻得越快=频率越高=音调越高。频率由 Freq_TH0/TL0 两个变量控制。  ║
; ║                                                                      ║
; ║  调用者: Main.asm（初始化时调用Timer0_Init）                           ║
; ║         Bsp_Buzzer.asm（写Freq_TH0/TL0然后 SETB TR0 启动定时器）      ║
; ║  中断:   Timer0溢出 → 自动触发 ORG 000BH → Timer0_ISR                ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第2部分：汇编指令速查表（本文件出现的所有指令）                        ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  ANL 直接地址, #n │ 按位"与"操作：把寄存器的某些位清零（只保留和#n中   ║
; ║                  │ 对应位都为1的位）。常用技巧：ANL X, #0F0H 清低4位   ║
; ║  ORL 直接地址, #n │ 按位"或"操作：把寄存器的某些位置1（#n中为1的位     ║
; ║                  │ 会被置1，其余保持）。                                 ║
; ║  CLR  bit        │ 把某个"位"清零（设为0）。CLR TR0 = 关掉定时器0     ║
; ║  SETB bit        │ 把某个"位"置1（设为1）。SETB ET0 = 允许定时器0中断 ║
; ║  MOV  直接地址, src│ 数据复制                                                   ║
; ║  CPL  bit        │ 把某个"位"取反（0变1，1变0）。CPL P2.4 = 翻转引脚  ║
; ║  RET             │ 从普通子函数返回                                    ║
; ║  RETI            │ 从中断服务函数返回（比RET多做一步：重新开放中断允许） ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第3部分：特殊功能寄存器速查表（本文件涉及的SFR）                       ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  TMOD  │ 89H │ 定时器模式寄存器。不能位寻址，只能用ANL/ORL改。         ║
; ║        │     │ 高4位控制Timer1，低4位控制Timer0。01H=模式1(16位定时器) ║
; ║  TCON  │ 88H │ 定时器控制寄存器。可位寻址。TR0=启动/停止，TF0=溢出标志 ║
; ║  TH0   │ 8CH │ Timer0计数初值高8位。从初值一直加到65535，溢出时        ║
; ║  TL0   │ 8AH │ Timer0计数初值低8位。溢出后需重新装填。                 ║
; ║  IE    │ A8H │ 中断允许寄存器。ET0(bit1)=允许Timer0中断，              ║
; ║        │     │ EA(bit7)=总中断开关（最高级别的"总闸"）                  ║
; ║  P2    │ A0H │ P2端口。P2.4是这个端口的第5个引脚（从0开始数）。        ║
; ║        │     │ 它连接着蜂鸣器，高电平=蜂鸣器不响，低电平=响。           ║
; ╚══════════════════════════════════════════════════════════════════════╝

NAME    DRV_TIMER

PUBLIC  Timer0_Init
PUBLIC  Timer0_ISR
PUBLIC  Freq_TH0
PUBLIC  Freq_TL0

TIMER_DATA  SEGMENT DATA
RSEG    TIMER_DATA
Freq_TH0:   DS  1 ; 申请一个字节的空间
Freq_TL0:   DS  1

TIMER_CODE  SEGMENT CODE
RSEG    TIMER_CODE

Timer0_Init:
	; 为何用ANL+ORL而非MOV TMOD,#01H？
	; TMOD的高4位属于Timer1（给串口用），MOV会连高4位一起覆盖，
	; 导致串口的波特率被破坏。ANL只清低4位，ORL只改低4位，互不干扰。
	ANL     TMOD, #0F0H ; 清除低4位，保留高4位（Timer1的设置不动）
	ORL     TMOD, #01H ; 低4位=0001B: GATE=0, C/T=0(定时模式), M1M0=01(模式1:16位)
	CLR     TR0 ; 先停止定时器0——初始化时不运行，等Buzzer_Play来 SETB TR0 才启动
	CLR     TF0 ; 清除溢出标志（刚上电可能残留垃圾值）
	SETB    ET0 ; 允许Timer0中断——溢出时CPU会放下手头工作跳去执行Timer0_ISR
	SETB    EA ; 开总中断——EA是整个中断系统的"总闸门"，不开则所有中断都被拦截
	RET

Timer0_ISR:
	; Timer0是模式1（16位），计数范围0~65535，溢出后自动归零。
	; 如果不清零且不重装初值，频率就跑飞了。所以每次进ISR第一件事：
	; 把Buzzer_Play计算的TH0/TL0初值重新填进去。
	MOV     TH0, Freq_TH0 ; 装载预设初值高8位
	MOV     TL0, Freq_TL0 ; 装载预设初值低8位
	; 关键：先装TH0再装TL0！因为装TL0时如果溢出会触发TH0联动增量，
	; 先装TH0可以避免这个微小的时序误差。
	CPL     P2.4 ; 翻转P2.4——如果之前是1就变0，之前是0就变1
	; 每次ISR翻转一次 = 波形的一个"半周期"。
	; 两次ISR才完成一个完整方波周期，所以实际声音频率 = 溢出频率 / 2。
	RETI ; 中断返回（自动重新开放中断优先级，普通RET做不到）

END
