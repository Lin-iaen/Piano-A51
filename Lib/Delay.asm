NAME    DELAY

PUBLIC  Delay_ms

DELAY_SEG   SEGMENT CODE
RSEG    DELAY_SEG

; Delay_ms
; Input : R7 = delay time in milliseconds
; Clock : 11.0592MHz (1 machine cycle = 12 / 11.0592MHz = 1.085us)
; Note  : 1ms loop is calibrated to about 1003.7us.
Delay_ms:
	PUSH    ACC
	PUSH    05H ; PUSH只能操作直接地址,不能写成PUSH R5,PUSH R6,PUSH R7
	PUSH    06H ; 05H是R5的地址，06H是R6，07H是R7——8051的R0~R7位于00H~07H
	PUSH    07H

	MOV     A, R7
	JZ      Delay_ms_Exit ; JZ (Jump if Zero) 指令只会检测累加器A，不能检测其他寄存器
	; 如果R7=0（不需要延时），直接跳过。为什么必须这样？
	; DJNZ会先把R7减1变成255，然后循环256次——0ms请求变成256ms，天差地别。

Delay_ms_Loop:
	MOV     R5, #02H ; 1机器周期(MC) = 1.085us, 1ms ≈ 922MC

Delay_ms_L1:
	MOV     R6, #0E5H ; 0E5H = 229, 2MC

Delay_ms_L2:
	DJNZ    R6, Delay_ms_L2 ; 内层循环，消耗 2MC × 229 = 458MC
	DJNZ    R5, Delay_ms_L1 ; 外层循环，消耗 2 × (458+2) ≈ 920MC
	; 加上PUSH/POP/LCALL/RET/MOV/JZ等固定开销约22MC，总计约942MC
	; 942 × 1.085us ≈ 1022us ≈ 1ms / 次。每个R7的外循环就是约1ms。
	DJNZ    R7, Delay_ms_Loop

Delay_ms_Exit:
	POP     07H ; 恢复寄存器——注意"后进先出"：先POP 07H（最后PUSH的）
	POP     06H
	POP     05H
	POP     ACC
	RET

END
