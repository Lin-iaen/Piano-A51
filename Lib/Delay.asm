; ║  白话:   让 CPU 数着时钟周期原地空转，空转的时间由你指定（R7=多少毫秒）。║
; ║          CPU 不干活，只是浪费固定数量的机器周期来"拖时间"。             ║
; ║          这是最"笨"但最简单的延时方式——不像闹钟那样有中断，它就是       ║
; ║          纯手工数数，所以叫"软件延时"。                                ║
; ║                                                                      ║
; ║  调用者: MusicPlayer.asm（音符之间的停顿）                             ║
