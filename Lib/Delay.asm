; ║  第2部分：汇编指令速查表（本文件出现的所有指令）                        ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  PUSH 直接地址 │ 把该地址的值压入"堆栈"保存（像摞盘子，后进先出）       ║
; ║               │ 注意：PUSH只能接直接地址，不能写 PUSH R5，要写 PUSH 05H ║
; ║  POP  直接地址 │ 从堆栈顶部弹出一个值恢复到该地址（与PUSH配对使用）      ║
; ║  MOV  A, R7   │ 把寄存器R7的值复制一份到累加器A。A是CPU最常用的        ║
; ║               │ "万能工作台"，大多数运算都要经过它。                   ║
; ║  JZ   标签     │ Jump if Zero：如果A的值等于0，就跳到标签处执行         ║
; ║  MOV  Rn, #n  │ 把立即数#n（一个写死的常数）直接写入Rn寄存器           ║
; ║  DJNZ Rn, 标签│ Decrement and Jump if Not Zero：先把Rn减1，           ║
; ║               │ 如果减完不等于0就跳到标签。这是汇编里最常用的循环指令。  ║
; ║  RET          │ 从子函数返回调用者（把之前LCALL压入的返回地址弹出来）    ║
; ╚══════════════════════════════════════════════════════════════════════╝
