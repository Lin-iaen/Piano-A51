/*
 * File: Bsp_Key.asm
 * Description: 
 *   本文件实现了一个无阻塞的按键扫描函数(软状态机)。
 *   为了避免主循环使用延时(Delay)而发生阻塞导致卡顿，这里采用了"状态机 + 轮询计数"的方式
 *   实现了非阻塞的按键防抖(Debounce)。
 *
 * C语言伪代码辅助理解:
 *   unsigned char Key_State = 0; // 全局状态变量
 *   unsigned char Key_Cnt = 0;   // 全局计数变量
 *   unsigned char Key_Temp = 0;  // 保存按键的特征标志
 *
 *   unsigned char Key_Scan() {
 *       unsigned char R0 = (~P3) & 0x38; // 提取引脚电平
 *       switch(Key_State) {
 *           case 0:
 *               if (R0 != 0) { Key_Temp = R0; Key_State = 1; Key_Cnt = 0; }
 *               return 0;
 *           case 1:
 *               if (++Key_Cnt == 250) { 
 *                   Key_State = 2; // 消抖结束，进入等起状态
 *                   if (R0 == Key_Temp) return mapKey(R0); 
 *               }
 *               return 0;
 *           case 2:
 *               if (R0 == 0) { Key_State = 3; Key_Cnt = 0; }
 *               return 0;
 *           case 3:
 *               if (++Key_Cnt == 250) {
 *                   Key_State = 0;
 *                   if (R0 == 0) return 5; // 5代表松手
 *               }
 *               return 0;
 *       }
 *   }
 */

; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第1部分：文件名片                                                   ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  文件名: Bsp_Key.asm                                                 ║
; ║  层级:   Bsp (板级支持层)                                            ║
; ║  功能:   扫描P3端口的3个物理按键，用"消抖状态机"过滤掉按键抖动噪音   ║
; ║          只返回确凿无疑的按键事件。                                  ║
; ║                                                                      ║
; ║  解释:   按下一个按钮时，金属触点并不是立刻稳定接触的——它会在        ║
; ║          几毫秒内疯狂弹跳，导致CPU在极短时间里看到                   ║
; ║          几十次"按下-松开-按下-松开"。                               ║
; ║          这个文件用一个4状态的"保险柜"来过滤这些假动作：             ║
; ║          发现按下→闭眼等15ms→再看，还在按？→确认按下                 ║
; ║          发现松开→闭眼等15ms→再看，真的松了？→确认松开(触发静音)！   ║
; ║          如果15ms后状态变了=刚才那是噪音抖动，直接忽略。             ║
; ║                                                                      ║
; ║  调用者: Main.asm 主循环每轮都调 Key_Scan                            ║
; ║  返回值: 0=无事件, 1/2/3=按键按下, 5=确认松开                        ║
; ║  键位:   P3.3=K1, P3.4=K2, P3.5=K3 (P3.2已被红外INT0占用)            ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第2部分：汇编指令速查表（本文件出现的所有指令）                     ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  MOV  A, P3    │ 读取P3端口的当前电平状态到A（读引脚）               ║
; ║  CPL  A        │ 把A的每个位取反（0变1,1变0）。按键按下时引脚为0，   ║
; ║               │ 取反后变成1，方便后续用JZ/JNZ判断"有没有键按下"      ║
; ║  ANL  A, #n    │ 按位"与"：保留#n中为1的那些位（掩码过滤）           ║
; ║               │ ANL A,#38H (0011 1000B) = 只保留bit3/4/5（P3.3~P3.5）║
; ║  JZ   标签      │ A=0就跳转      (JUMP If Zero)                      ║
; ║  JNZ  标签      │ A≠0就跳转                                          ║
; ║  DEC  A        │ A减1                                                ║
; ║  INC  直接地址  │ 把指定地址的值加1                                  ║
; ║  CJNE A, #n, 标签│ 比较A和n，不相等就跳转。                          ║
; ║  3字节指令,跳转范围只有前后128B,且A>n时CY会被置0,A<1时CY会被置1      ║
; ║  MOV  直接地址, #n│ 把立即数n写入内存                                ║
; ║  SJMP 标签      │ 无条件短跳转                                       ║
; ║  CLR  A        │ 把A清零（汇编习惯用CLR A代替MOV A,#0，省一个字节）  ║
; ║  RET           │ 返回调用者                                          ║
; ║  DS   n        │ 伪指令：在RAM中预留n个字节空间（Define Storage）    ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第3部分：特殊功能寄存器速查表（本文件涉及的SFR）                    ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  P3 │ B0H │ P3端口。这里只读它的bit3/4/5（P3.3, P3.4, P3.5按键）。   ║
; ║     │     │ P3.2已被红外占用为INT0，所以按键扫描时用ANL #38H过滤掉   ║
; ╚══════════════════════════════════════════════════════════════════════╝

NAME    BSP_KEY
        PUBLIC  Key_Scan

        KEY_DATA SEGMENT DATA
        RSEG    KEY_DATA
Key_State:  DS  1       ; 状态机当前状态: 0=等按下, 1=按下消抖, 2=等松开, 3=松开消抖
Key_Cnt:    DS  1       ; 快照延时计数器（数到250=约15ms）
Key_Temp:   DS  1       ; 暂存按键特征（'快照'用于消抖比对）

        KEY_CODE SEGMENT CODE
        RSEG    KEY_CODE

; ========================================================
; 函数：Key_Scan (快照消抖状态机)
; ========================================================
Key_Scan:
        MOV     A, P3   ; 读取P3端口全部8个引脚的电平
        CPL     A       ; 取反：按键按下=低电平(0)→取反后变1，便于后续判断
        ; P3.2(INT0) 已给红外使用，这里仅扫描 P3.3/P3.4/P3.5 三个按键
        ; 38H = 0011 1000B，按位"与"后只保留bit3/4/5，其余位清零
        ANL     A, #038H
        MOV     R0, A   ; R0 暂存当前有效的按键特征

        ; --- 状态机路由：根据Key_State的值跳到不同状态处理 ---
        MOV     A, Key_State
        ; JZ不会改变A的值，本质上是累加器后面通过一个8位输入的或非门来实现的条件跳转,只要A中有一个位为1经过或非门后结果为0,不满足为0的条件,就跳过JZ执行下一条指令
        JZ      STATE_0         ; Key_State=0 → 等待按下
        DEC     A               ; Key_State-1=0?
        JZ      STATE_1         ; Key_State=1 → 按下消抖中
        DEC     A               ; Key_State-2=0?
        JZ      STATE_2         ; Key_State=2 → 等待松开
        SJMP    STATE_3         ; 剩下的只有 Key_State=3 → 松开消抖中

STATE_0: ; --- 状态 0：等待按下 ---
        MOV     A, R0           ; 把当前按键特征加载到A
        JZ      KEY_RET_0       ; R0=0说明没任何按键,跳转到KEY_RET_0返回0=无事件
        
        MOV     Key_Temp, A     ; 记录第一次按下的特征（哪个键被按了）
        MOV     Key_State, #1   ; 进入状态 1：开始"闭眼等消抖"
        MOV     Key_Cnt, #0     ; 重置消抖计数器
        SJMP    KEY_RET_0       

STATE_1: ; --- 状态 1：按下快照消抖 ---
        INC     Key_Cnt         ; 计数器+1
        MOV     A, Key_Cnt
        CJNE    A, #150, KEY_RET_0 ; 还没到150（约15ms），继续等
        
        ; 跑够150次循环了,大约15ms,再次检查按键状态，看看是抖动噪音还是确实按下了
        MOV     Key_State, #2   ; 无论结果，下一步都进入"等松开"状态
        
        ; 快照比对：现在的引脚状态(R0) 和 15ms前记录的特征(Key_Temp) 一样吗？
        MOV     A, R0
        CJNE    A, Key_Temp, KEY_RET_0 ; 不相等就跳转到KEY_RET_0返回0=无事件
        
        ; 一模一样=确认为有效按键  返回对应的键码
        ; 注：00=没键, 04H=K1(P3.3), 08H=K2(P3.4), 10H=K3(P3.5)
        CJNE    A, #00000100B, CHECK_K2  ; 不是K1就去检查K2
        MOV     A, #1           ; 如果就是Key1被按下,K1→返回1
        RET
CHECK_K2:
        CJNE    A, #00001000B, CHECK_K3  ; 不是K2就去检查K3
        MOV     A, #2           ; K2→返回2
        RET
CHECK_K3:
        CJNE    A, #00010000B, KEY_RET_0 ; 也不是K3那只能是噪音，丢弃
        MOV     A, #3           ; K3→返回3
        RET

STATE_2: ; --- 状态 2：等待松开 ---
        MOV     A, R0
        JNZ     KEY_RET_0       ; R0还有值=键还按着，继续等
        
        ; 发现松开了 进入"松开消抖"
        MOV     Key_State, #3   ; 进入状态 3：闭眼等
        MOV     Key_Cnt, #0
        SJMP    KEY_RET_0

STATE_3: ; --- 状态 3：松开快照消抖 ---
        INC     Key_Cnt
        MOV     A, Key_Cnt
        CJNE    A, #150, KEY_RET_0 ; 闭眼等够约15ms
        
        ; 延时消抖同上
        MOV     Key_State, #0   ; 无论结果，回归状态0（等待按下）
        
        MOV     A, R0
        JNZ     KEY_RET_0       ; 居然又有值了（电源噪音），忽略！
        
        ; 完美松开后返回5→Main.asm的Key_ToEvent会把它映射为静音事件(22)
        MOV     A, #5
        RET

KEY_RET_0:
        CLR     A               ; 返回0=无事件
        RET

        END