; ║  第1部分：文件名片                                                     ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  文件名: Bsp_Led.asm                                                  ║
; ║  层级:   Bsp (板级支持层)                                             ║
; ║  功能:   根据当前音符的编号(0~21)，查表找到对应的LED灯效，输出到P1端口。 ║
; ║                                                                      ║
; ║  白话:   LED灯怎么知道该亮哪些？这里存了一张"字典表"：音符0→全灭，      ║
; ║          低音→左边亮1~2个灯，中音→中间亮，高音→全亮/交替闪。           ║
; ║          P1端口的8个引脚连接着8个LED，写"0"的那一位=LED亮，             ║
; ║          写"1"的那一位=LED灭。所以 11111110B 表示只亮最右边一个灯。     ║
; ║                                                                      ║
; ║  调用者: Main.asm 的 Dispatch_Event（每次按键/串口/红外触发都调）       ║
; ║         MusicPlayer.asm（播放歌曲时每个音符也调）                      ║
; ║  依赖:   无（只需要P1端口，不依赖其他函数）                             ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第2部分：汇编指令速查表（本文件出现的所有指令）                        ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  PUSH 直接地址  │ 把值压入堆栈保存（总是成对使用：先PUSH后POP）        ║
; ║  POP  直接地址  │ 从堆栈弹出恢复。注意顺序：最后PUSH的最先POP           ║
; ║  MOV  DPTR, #n │ 把16位地址#n写入DPTR（数据指针寄存器）               ║
; ║  MOVC A, @A+DPTR│ 查表指令！从程序存储器(ROM)中读取 A+DPTR 偏移处的   ║
; ║                │ 一个字节，存入A。DB定义的常数存在ROM里，用MOVC读取。   ║
; ║  MOV  P1, A    │ 把A的值写到P1端口——直接驱动8个LED                     ║
; ║  DB   数据      │ 伪指令：Define Byte，在ROM中存放一个字节（表格数据）   ║
; ║  RET           │ 返回调用者                                          ║
; ╚══════════════════════════════════════════════════════════════════════╝
;
; ╔══════════════════════════════════════════════════════════════════════╗
; ║  第3部分：特殊功能寄存器速查表（本文件涉及的SFR）                       ║
; ╠══════════════════════════════════════════════════════════════════════╣
; ║  DPTR │ 82H+83H │ 16位数据指针。PUSH/POP时必须拆成82H(DPL低8位)和    ║
; ║       │          │ 83H(DPH高8位)两个8位地址分别操作，8051无PUSH DPTR ║
; ║  P1   │ 90H      │ P1端口。8个I/O引脚，这里每个引脚驱动一个LED。      ║
; ║       │          │ 写0=LED亮（低电平驱动），写1=LED灭。               ║
; ╚══════════════════════════════════════════════════════════════════════╝

NAME    BSP_LED

        PUBLIC  Led_ShowSpectrum  ; 导出函数，让 Protocol 层可以调用

        LED_CODE SEGMENT CODE
        RSEG    LED_CODE

; ========================================================
; 函数：Led_ShowSpectrum
; 功能：根据输入的音符索引，点亮对应的 LED 频谱条
; 入口：ACC (0~21)
; 破坏：ACC, DPTR (已做内部压栈保护)
; ========================================================
Led_ShowSpectrum:
	; --- 1. 保护现场 ---
	; DPTR是一个16位寄存器（由DPH+ DPL拼成），8051没有直接PUSH DPTR的指令，
	; 必须拆成 PUSH 82H(DPL) 和 PUSH 83H (DPH) 分别保存。
        PUSH    ACC     ; 保护原有的 ACC
        PUSH    82H     ; 保护 DPL (82H)
        PUSH    83H     ; 保护 DPH (83H)

        ; --- 2. 核心查表与输出  ---
        MOV     DPTR, #LED_PATTERN_TABLE ; 把DPTR指向表格首地址
        ; MOVC = 从程序存储器(ROM)中读取。A存的是音符索引（偏移量），
        ; A+DPTR = 表格首地址+偏移量 = 目标数据的ROM地址。
        ; 读取结果依然放在A里，覆盖了原来的音符索引。
	MOVC    A, @A+DPTR ; 从表格中把灯光数据抓到A
        
        MOV     P1, A   ; 把灯光数据写到P1端口，每个bit控制一个LED：0=亮,1=灭
        

        ; --- 3. 恢复现场 ---
        ; 注意顺序：先PUSH的A最后POP（后进先出原则）
        POP     83H     ; 恢复 DPH
        POP     82H     ; 恢复 DPL
        POP     ACC     ; 恢复 ACC
        RET


; ========================================================
; 频谱映射表 (0表示灯亮，1表示灯灭)
; 为什么是"0=亮"？因为51单片机的I/O引脚灌电流能力比拉电流强，
; 所以一般LED的负极接引脚、正极接VCC，引脚输出0时LED导通发光。
; ========================================================
LED_PATTERN_TABLE:
        ; 索引 0: 休止符 (全灭)
        DB  11111111B   
        
        ; 索引 1~7: 低音区 (点亮左边 1~2 个灯，像能量条刚起步)
        DB  11111110B, 11111100B, 11111000B, 11111100B, 11111110B, 11111100B, 11111000B
        
        ; 索引 8~14: 中音区 (点亮中间的灯)
        DB  11110000B, 11100000B, 11000000B, 10000000B, 11000000B, 11100000B, 11110000B
        
        ; 索引 15~21: 高音区 (全亮或者对称V型效果)
        DB  01111110B, 00111100B, 00011000B, 00000000B, 00011000B, 00111100B, 01111110B

        END