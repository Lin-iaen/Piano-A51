; ║  功能:   将音符编号(0~21)翻译成数字，写入数码管驱动层的显示缓冲。        ║
; ║         提供统一的"显示数字"接口——和 Led_ShowSpectrum 一样，             ║
; ║         ACC 传入音符编号，0=灭，1~21=显示对应的数字。                    ║
