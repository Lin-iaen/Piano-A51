NAME    BSP_DIGITUBE

PUBLIC  DigiTube_ShowNumber
PUBLIC  DigiTube_Off

EXTRN   CODE (DigiTube_SetNum)
EXTRN   CODE (DigiTube_Clear)

BSP_DIGITUBE_CODE SEGMENT CODE
RSEG    BSP_DIGITUBE_CODE

; ========================================================
; DigiTube_ShowNumber — 在数码管上显示音符编号
; 入口: ACC = 音符编号 (0~21)
;       0 = 熄灭数码管（静音）
;       1~21 = 显示该数字（1~21）
; 破坏: ACC (内部已保护)
; ========================================================
DigiTube_ShowNumber:
    PUSH    ACC

    JZ      ShowNumber_Off     ; A=0 → 熄灭

    ; 范围保护：>21 的非法值忽略
    CLR     C
    SUBB    A, #22             ; A = A - 22
    JNC     ShowNumber_Exit    ; CY=0 → A>=22 → 非法，不显示

    ; 合法值 1~21，恢复ACC并显示
    POP     ACC
    PUSH    ACC

    PUSH    82H
    PUSH    83H

    LCALL   DigiTube_SetNum    ; 写入数码管缓冲

    POP 83H
    POP 82H

    SJMP    ShowNumber_Exit

ShowNumber_Off:
    LCALL   DigiTube_Clear     ; 清空所有位

ShowNumber_Exit:
    POP     ACC
    RET

; ========================================================
; DigiTube_Off — 熄灭数码管（等同于 ShowNumber(0)）
; 破坏: 无
; ========================================================
DigiTube_Off:
    PUSH    ACC
    LCALL   DigiTube_Clear
    POP     ACC
    RET

    END
