; ║          数码管更直观，直接把音符编号（1,2,3...21）用数字显示出来。       ║
; ║          这个文件就是把"音符编号"翻译成数码管要显示的"数字"。              ║
; ║          类比 Led_ShowSpectrum：都是 ACC 传入音符编号，                  ║
; ║          一个输出到P1(LED)，一个输出到数码管缓冲(Drv_DigiTube)。          ║
; ║                                                                        ║
; ║  调用者: Main.asm 的 Dispatch_Event（单音/静音事件触发）                 ║
; ║         MusicPlayer.asm（播放每个音符时触发）                            ║
; ║  依赖:   Drv_DigiTube.asm (DigiTube_SetNum, DigiTube_Clear)             ║
